        "# Implements:\n",
        "#   * Custom GRU and MGU cells using basic PyTorch ops\n",
        "#   * Shallow RNN classifier that treats each image row as a time step\n",
        "#   * 3 trials for GRU and 3 trials for MGU\n",
        "#   * Training vs test accuracy plots for each trial (6 PNGs)\n",
        "#   * Final test accuracies and mean classification errors\n",
        "# ============================================================\n",
        "\n",
        "import torch\n",
        "import torch.nn as nn\n",
        "import torch.optim as optim\n",
        "from torch.utils.data import DataLoader, Subset\n",
        "import torchvision\n",
        "import torchvision.transforms as transforms\n",
        "import matplotlib.pyplot as plt\n",
        "import numpy as np\n",
